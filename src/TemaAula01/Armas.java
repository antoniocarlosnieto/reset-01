package TemaAula01;

public class Armas {
        class EspadaCurta {
            String nome;
            int poder;

            EspadaCurta(String nome, int poder) {
                this.nome = nome;
                this.poder = poder;
            }
        }
        class EspadaLonga {
            String nome;
            int poder;

            EspadaLonga(String nome, int poder) {
                this.nome = nome;
                this.poder = poder;
            }
        }
        class Machado {
            String nome;
            int poder;

            Machado(String nome, int poder) {
                this.nome = nome;
                this.poder = poder;
            }
        }
    }

